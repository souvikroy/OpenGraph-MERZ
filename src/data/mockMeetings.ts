export const getUpcomingMeetings = () =>
  mockMeetings.filter(m => m.status === 'upcoming').sort((a, b) => {
    const da = new Date(`${a.date}T${a.time}`);
    const db = new Date(`${b.date}T${b.time}`);
    return da.getTime() - db.getTime();
  });

export const getPastMeetings = () =>
  mockMeetings.filter(m => m.status === 'completed');

export const getMeetingById = (id: string) => mockMeetings.find(m => m.id === id);
